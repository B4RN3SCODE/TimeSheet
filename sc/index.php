function setCurrentTime()
{
	// Get 24-hour clock
	var d = new Date;
	var h = d.getHours();
	var m = d.getMinutes();
	var s = d.getSeconds();

	// Get 12-hour period
	var p = h >= 12 ? 'pm' : 'am';

	// Convert to 12-hour clock
	h = h % 12;
	h = h ? h : 12; // Zero hour is the same as 12 am

	// Adding padding to min and seconds
	m = m < 10 ? '0' + m : m;
	s = s < 10 ? '0' + s : s;

	// Return time as an object
	document.getElementById("time").innerHTML = h + ':' + m + '<small>' + s + '</small> ' + p;
}

